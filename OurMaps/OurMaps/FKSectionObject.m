#import "FKSectionObject.h"

////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////
@implementation FKSectionObject

@synthesize headerTitle, footerTitle;


////////////////////////////////////////////////////////////////////////////////////////////////////
+ (id)sectionWithHeaderTitle:(NSString *)title footerTitle:(NSString *)footerTitle {
    FKSectionObject *object = [[FKSectionObject alloc] init];
    object.headerTitle = title;
    object.footerTitle = footerTitle;
    return object;
}


@end
